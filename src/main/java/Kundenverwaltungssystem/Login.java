    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = this.jTextField1.getText();
        String password = new String(this.jPasswordField1.getPassword());
        
        if(username.length() ==0 || password.length() == 0) {
            JOptionPane.showMessageDialog(null, "Geben Sie bitte gï¿½ltige Daten ein!", "Fehler", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!new AdminService().login(username, password)) {
            JOptionPane.showMessageDialog(null, "Ihre login-Daten sind falsch!", "Fehler", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        new KundeUI(username).setVisible(true);
        super.dispose();

    }//GEN-LAST:event_jButton1ActionPerformed

